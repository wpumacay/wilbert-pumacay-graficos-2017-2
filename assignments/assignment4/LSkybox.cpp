#include "LSkybox.h"
#include "LAssetsManager.h"

using namespace std;

namespace engine
{


    LSkybox::LSkybox( string skyboxResId )
    {

        GLfloat _vertices[] =
        {
            -1.0f,  1.0f, -1.0f,
            -1.0f, -1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,
             1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,

            -1.0f, -1.0f,  1.0f,
            -1.0f, -1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f, -1.0f,
            -1.0f,  1.0f,  1.0f,
            -1.0f, -1.0f,  1.0f,

             1.0f, -1.0f, -1.0f,
             1.0f, -1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,

            -1.0f, -1.0f,  1.0f,
            -1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f, -1.0f,  1.0f,
            -1.0f, -1.0f,  1.0f,

            -1.0f,  1.0f, -1.0f,
             1.0f,  1.0f, -1.0f,
             1.0f,  1.0f,  1.0f,
             1.0f,  1.0f,  1.0f,
            -1.0f,  1.0f,  1.0f,
            -1.0f,  1.0f, -1.0f,

            -1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f,  1.0f,
             1.0f, -1.0f, -1.0f,
             1.0f, -1.0f, -1.0f,
            -1.0f, -1.0f,  1.0f,
             1.0f, -1.0f,  1.0f
        };

        auto _vBuffer = new LVertexBuffer();
        _vBuffer->setData( sizeof( GLfloat ) * 3 * 36,
                           3, _vertices );

        m_vertexArray = new LVertexArray();
        m_vertexArray->addBuffer( _vBuffer, 0 );

        m_cubeTexture = new LCubeTexture();
        m_cubeTexture->setData( LAssetsManager::INSTANCE->cubeTexturesData[skyboxResId],
                                GL_RGB, 0 );
    }

    LSkybox::~LSkybox()
    {
        delete m_vertexArray;
    }



    void LSkybox::render()
    {
        m_cubeTexture->bind();
        m_vertexArray->bind();

        glDrawArrays( GL_TRIANGLES, 0, 36 );

        m_vertexArray->unbind();
        m_cubeTexture->unbind();
    }
}